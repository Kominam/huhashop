    public function markAsSent($id)
    {
        $order = Order::find($id);
        if ($order) {
            $order->is_sent = 1;
            $order->save();
            Mail::to($order->user->email)->send(new OrderCompleteMail($order));
            return redirect()->route('admin.order.index')->with('statusMarkAsSent', 'success');
        }
        return back();
    }
